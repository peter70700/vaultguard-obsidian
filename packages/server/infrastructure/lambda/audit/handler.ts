/**
 * Queries audit logs with flexible filtering.
 *
 * Query Parameters:
 * - userId: Filter by user who performed the action
 * - action: Filter by action type (e.g., 'files.read', 'auth.login')
 * - path: Filter by resource path (prefix match)
 * - outcome: Filter by outcome ('success', 'denied', 'error')
 * - startDate: ISO timestamp for range start
 * - endDate: ISO timestamp for range end
 * - limit: Maximum results (default 100, max 1000)
 * - lastEvaluatedKey: For pagination
 *
 * @param event - API Gateway event with query parameters
 * @param user - Authenticated admin user
 * @param requestId - Request ID for tracing
 * @returns Paginated audit log entries matching filters
 */
async function handleQueryLogs(
  event: APIGatewayProxyEvent,
  user: UserContext,
  vaultId: string,
  requestId: string
): Promise<APIGatewayProxyResult> {
  const params = event.queryStringParameters || {};
  const limit = clampLimit(params.limit, 100, 1000);
  const retentionWindow = await applyRetentionWindow(
    user.orgId,
    normalizeStartTimestamp(params.startDate),
    normalizeEndTimestamp(params.endDate)
  );

  const filters: AuditQueryFilters = {
    search: params.search || params.q,
    userId: params.userId,
    action: params.action,
    resourcePath: params.path,
    outcome: params.outcome as AuditQueryFilters['outcome'],
    startDate: retentionWindow.startDate,
    endDate: retentionWindow.endDate,
    limit,
    cursor: decodeCursor(params.cursor),
  };
  const query = buildVaultAuditQuery(user.orgId, vaultId, filters);
  const result = await queryAuditIndex<AuditEntry>({
    indexName: 'vaultId-index',
    keyConditionExpression: query.keyConditionExpression,
    expressionAttributeValues: query.expressionAttributeValues,
    expressionAttributeNames: query.expressionAttributeNames,
    filterExpression: query.filterExpression,
    limit,
    cursor: filters.cursor,
  });

  return formatSuccess(
    200,
    {
      entries: result.items,
      count: result.items.length,
      filters,
      nextCursor: result.nextCursor,
      lastEvaluatedKey: result.lastEvaluatedKey,
    },
    requestId
  );
}

// ─── GET /vaults/{vaultId}/audit/user/{userId} ───────────────────────────────

/**
 * Returns all audit activity for a specific user.
 * Shows a complete timeline of their actions in the vault.
 *
 * Query Parameters:
 * - startDate: Filter from this date (optional)
 * - endDate: Filter to this date (optional)
 * - limit: Maximum results (default 200)
 *
 * @param event - API Gateway event with userId path parameter
 * @param user - Authenticated admin user
 * @param requestId - Request ID for tracing
 * @returns User's complete activity timeline
 */
async function handleUserActivity(
  event: APIGatewayProxyEvent,
  user: UserContext,
  vaultId: string,
  requestId: string
): Promise<APIGatewayProxyResult> {
  const targetUserId = event.pathParameters?.userId;

  if (!targetUserId) {
    return formatError(400, 'userId path parameter is required', requestId);
  }

  const params = event.queryStringParameters || {};
  const limit = clampLimit(params.limit, 200, 1000);
  const retentionWindow = await applyRetentionWindow(
    user.orgId,
    normalizeStartTimestamp(params.startDate),
    normalizeEndTimestamp(params.endDate)
  );
  const startDate = retentionWindow.startDate;
  const endDate = retentionWindow.endDate;
  const cursor = decodeCursor(params.cursor);
  const query = buildTimeBoundKeyCondition('vaultId = :vaultId', {
    ':vaultId': vaultId,
  }, startDate, endDate);
  query.filterParts.push('orgId = :orgId');
  query.expressionAttributeValues[':orgId'] = user.orgId;
  query.filterParts.push('userId = :userId');
  query.expressionAttributeValues[':userId'] = targetUserId;

  const result = await queryAuditIndex<AuditEntry>({
    indexName: 'vaultId-index',
    keyConditionExpression: query.keyConditionExpression,
    expressionAttributeValues: query.expressionAttributeValues,
    expressionAttributeNames: query.expressionAttributeNames,
    filterExpression: query.filterParts.join(' AND '),
    limit,
    cursor,
  });

  // Generate activity summary
  const summary = generateActivitySummary(result.items);

  return formatSuccess(
    200,
    {
      userId: targetUserId,
      entries: result.items,
      count: result.items.length,
      summary,
      nextCursor: result.nextCursor,
      lastEvaluatedKey: result.lastEvaluatedKey,
    },
    requestId
  );
}

// ─── GET /vaults/{vaultId}/audit/file/{path} ─────────────────────────────────

/**
 * Returns all access records for a specific file path.
 * Shows who accessed the file, when, and what actions were performed.
 *
 * Query Parameters:
 * - startDate: Filter from this date (optional)
 * - endDate: Filter to this date (optional)
 * - limit: Maximum results (default 200)
 *
 * @param event - API Gateway event with file path parameter
 * @param user - Authenticated admin user
 * @param requestId - Request ID for tracing
 * @returns File access history with user details
 */
async function handleFileAccess(
  event: APIGatewayProxyEvent,
  user: UserContext,
  vaultId: string,
  requestId: string
): Promise<APIGatewayProxyResult> {
  const filePath = '/' + decodeURIComponent(event.pathParameters?.path || '');

  if (filePath === '/') {
    return formatError(400, 'File path is required', requestId);
  }

  const params = event.queryStringParameters || {};
  const limit = clampLimit(params.limit, 200, 1000);
  const retentionWindow = await applyRetentionWindow(
    user.orgId,
    normalizeStartTimestamp(params.startDate),
    normalizeEndTimestamp(params.endDate)
  );
  const startDate = retentionWindow.startDate;
  const endDate = retentionWindow.endDate;
  const cursor = decodeCursor(params.cursor);
  const query = buildTimeBoundKeyCondition('resourcePath = :path', {
    ':path': filePath,
  }, startDate, endDate);
  query.filterParts.push('orgId = :orgId');
  query.expressionAttributeValues[':orgId'] = user.orgId;
  query.filterParts.push('vaultId = :vaultId');
  query.expressionAttributeValues[':vaultId'] = vaultId;

  const result = await queryAuditIndex<AuditEntry>({
    indexName: 'resourcePath-index',
    keyConditionExpression: query.keyConditionExpression,
    expressionAttributeValues: query.expressionAttributeValues,
    expressionAttributeNames: query.expressionAttributeNames,
    filterExpression: query.filterParts.join(' AND '),
    limit,
    cursor,
  });

  // Summarize unique users who accessed this file
  const uniqueUsers = [...new Set(result.items.map((e) => e.userId))];
  const actionBreakdown = result.items.reduce((acc, e) => {
    acc[e.action] = (acc[e.action] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);

  return formatSuccess(
    200,
    {
      path: filePath,
      entries: result.items,
      count: result.items.length,
      uniqueUsers,
      actionBreakdown,
      nextCursor: result.nextCursor,
      lastEvaluatedKey: result.lastEvaluatedKey,
    },
    requestId
  );
}

// ─── GET /vaults/{vaultId}/audit/alerts ──────────────────────────────────────

/**
 * Returns security alerts generated by the anomaly detection system.
 *
 * Alert types:
 * - revoked_access_attempt: A user whose access was revoked tried to authenticate
 * - unusual_pattern: Abnormal behavior detected (time, volume, pattern)
 * - bulk_download: Excessive file reads in a short window
 * - repeated_denial: Multiple permission-denied events for same user
 * - off_hours_access: Access attempts outside normal working hours
 *
 * Query Parameters:
 * - severity: Filter by severity level
 * - type: Filter by alert type
 * - acknowledged: 'true' or 'false' to filter by acknowledgment status
 * - limit: Maximum results (default 50)
 *
 * @param event - API Gateway event with optional filters
 * @param user - Authenticated admin user
 * @param requestId - Request ID for tracing
 * @returns List of security alerts
 */
async function handleGetAlerts(
  event: APIGatewayProxyEvent,
  user: UserContext,
  vaultId: string,
  requestId: string
): Promise<APIGatewayProxyResult> {
  const params = event.queryStringParameters || {};
  const limit = clampLimit(params.limit, 50, 500);
  const expressionValues: Record<string, unknown> = { ':orgId': user.orgId, ':vaultId': vaultId };
  const expressionNames: Record<string, string> = {};
  const filterParts: string[] = ['vaultId = :vaultId'];

  if (params.severity) {
    filterParts.push('severity = :severity');
    expressionValues[':severity'] = params.severity;
  }

  if (params.type) {
    filterParts.push('#type = :type');
    expressionValues[':type'] = params.type;
    expressionNames['#type'] = 'type';
  }

  if (params.acknowledged !== undefined) {
    filterParts.push('acknowledged = :ack');
    expressionValues[':ack'] = params.acknowledged === 'true';
  }

  await detectAnomalies(user, vaultId);

  const result = await queryAlerts({
    expressionAttributeValues: expressionValues,
    expressionAttributeNames: expressionNames,
    filterExpression: filterParts.length > 0 ? filterParts.join(' AND ') : undefined,
    limit,
    cursor: decodeCursor(params.cursor),
  });
  const alerts = sortAlerts(result.items);

  return formatSuccess(
    200,
    {
      alerts,
      count: alerts.length,
      unacknowledged: alerts.filter((a) => !a.acknowledged).length,
      nextCursor: result.nextCursor,
      lastEvaluatedKey: result.lastEvaluatedKey,
    },
    requestId
  );
}

// ─── POST /vaults/{vaultId}/audit/export ─────────────────────────────────────

/**
 * Exports audit logs as CSV for compliance reporting.
 *
 * Request body:
 * - startDate: ISO timestamp for export range start (required)
 * - endDate: ISO timestamp for export range end (required)
 * - userId: Optional filter by user
 * - actions: Optional array of action types to include
 * - format: 'csv' (default) or 'json'
 *
 * @param event - API Gateway event with export parameters in body
 * @param user - Authenticated admin user
 * @param requestId - Request ID for tracing
 * @returns CSV-formatted audit log data
 */
async function handleExport(
  event: APIGatewayProxyEvent,
  user: UserContext,
  vaultId: string,
  requestId: string
): Promise<APIGatewayProxyResult> {
  const body = parseBody(event);
  const now = new Date();
  const retentionWindow = await applyRetentionWindow(
    user.orgId,
    normalizeStartTimestamp(body.startDate as string | undefined)
      || new Date(now.getTime() - 30 * 24 * 60 * 60 * 1000).toISOString(),
    normalizeEndTimestamp(body.endDate as string | undefined)
      || now.toISOString()
  );
  const startDate = retentionWindow.startDate
    || new Date(now.getTime() - 30 * 24 * 60 * 60 * 1000).toISOString();
  const endDate = retentionWindow.endDate || now.toISOString();
  const filterUserId = body.userId as string | undefined;
  const search = body.search as string | undefined;
  const resourcePath = body.path as string | undefined;
  const filterAction = body.action as string | undefined;
  const filterActions = body.actions as string[] | undefined;
  const outcome = body.outcome as AuditQueryFilters['outcome'] | undefined;
  const format = (body.format as string) || 'csv';

  // Validate dates
  if (isNaN(new Date(startDate).getTime()) || isNaN(new Date(endDate).getTime())) {
    return formatError(400, 'Invalid date format. Use ISO 8601.', requestId);
  }
  const query = buildVaultAuditQuery(user.orgId, vaultId, {
    search,
    userId: filterUserId,
    action: filterAction,
    resourcePath,
    outcome,
    startDate,
    endDate,
  });
  let allEntries = await queryAllAuditEntries({
    indexName: 'vaultId-index',
    keyConditionExpression: query.keyConditionExpression,
    expressionAttributeValues: query.expressionAttributeValues,
    expressionAttributeNames: query.expressionAttributeNames,
    filterExpression: query.filterExpression,
  });

  // Apply action filter if provided
  if (filterActions && filterActions.length > 0) {
    allEntries = allEntries.filter((e) => filterActions.includes(e.action));
  }

  // Sort chronologically
  allEntries.sort((a, b) => new Date(a.timestamp).getTime() - new Date(b.timestamp).getTime());

  // Log the export action
  await logAudit({
    userId: user.userId,
    userEmail: user.email,
    orgId: user.orgId,
    vaultId,
    action: 'audit.export',
    resourcePath: `/vaults/${vaultId}/audit/export`,
    outcome: 'success',
    ipAddress: getClientIp(event),
    userAgent: getUserAgent(event),
    metadata: {
      startDate,
      endDate,
      vaultId,
      entryCount: allEntries.length,
      format,
      search,
      resourcePath,
      filterUserId,
      filterAction,
      filterActions,
      outcome,
    },
  });

  if (format === 'json') {
    return formatSuccess(
      200,
      {
        entries: allEntries,
        count: allEntries.length,
        exportedAt: new Date().toISOString(),
        range: { startDate, endDate },
      },
      requestId
    );
  }

  // Generate CSV
  const csv = generateCsv(allEntries);

  return {
    statusCode: 200,
    headers: {
      'Content-Type': 'text/csv',
      'Content-Disposition': `attachment; filename="audit-log-${startDate.split('T')[0]}-to-${endDate.split('T')[0]}.csv"`,
      'Access-Control-Allow-Origin': ALLOWED_CORS_ORIGIN,
      'Access-Control-Allow-Headers': 'Content-Type,Authorization,X-VaultGuard-Session-Id',
      'Access-Control-Allow-Methods': 'GET,POST,PUT,PATCH,DELETE,OPTIONS',
      'X-Request-Id': requestId,
    },
    body: csv,
  };
}

// ─── Anomaly Detection ───────────────────────────────────────────────────────

/**
 * Runs real-time anomaly detection against recent audit log entries.
 * Checks for:
 * 1. Revoked users attempting access
 * 2. Bulk download patterns (many reads in short window)
 * 3. Repeated permission denials for same user
 * 4. Access during unusual hours
 *
 * @returns Array of newly detected security alerts
 */
async function detectAnomalies(user: UserContext, vaultId: string): Promise<SecurityAlert[]> {
  const windowStart = new Date(Date.now() - ANOMALY_WINDOW_MINUTES * 60 * 1000).toISOString();
  const recentEntries = await queryAllAuditEntries({
    indexName: 'vaultId-index',
    keyConditionExpression: 'vaultId = :vaultId AND #ts >= :windowStart',
    expressionAttributeValues: {
      ':vaultId': vaultId,
      ':orgId': user.orgId,
      ':windowStart': windowStart,
    },
    expressionAttributeNames: {
      '#ts': 'timestamp',
    },
    filterExpression: 'orgId = :orgId',
  });
  const windowBucket = Math.floor(Date.now() / (ANOMALY_WINDOW_MINUTES * 60 * 1000));
  const alerts: SecurityAlert[] = [];

  // Detect: Repeated permission denials
  const denialsByUser = new Map<string, AuditEntry[]>();
  for (const entry of recentEntries) {
    if (entry.outcome === 'denied') {
      const existing = denialsByUser.get(entry.userId) || [];
      existing.push(entry);
      denialsByUser.set(entry.userId, existing);
    }
  }

  for (const [userId, denials] of denialsByUser) {
    if (denials.length >= ANOMALY_THRESHOLD_DENIED) {
      alerts.push({
        id: buildAlertId('repeated_denial', user.orgId, vaultId, userId, String(windowBucket)),
        orgId: user.orgId,
        vaultId,
        type: 'repeated_denial',
        severity: denials.length >= ANOMALY_THRESHOLD_DENIED * 2 ? 'high' : 'medium',
        userId,
        description: `User ${userId} received ${denials.length} permission denials in the last ${ANOMALY_WINDOW_MINUTES} minutes`,
        timestamp: new Date().toISOString(),
        metadata: {
          denialCount: denials.length,
          vaultId,
          paths: [...new Set(denials.map((d) => d.resourcePath))],
          actions: [...new Set(denials.map((d) => d.action))],
        },
        acknowledged: false,
      });
    }
  }

  // Detect: Bulk downloads
  const readsByUser = new Map<string, AuditEntry[]>();
  for (const entry of recentEntries) {
    if (entry.action === 'files.read' && entry.outcome === 'success') {
      const existing = readsByUser.get(entry.userId) || [];
      existing.push(entry);
      readsByUser.set(entry.userId, existing);
    }
  }

  for (const [userId, reads] of readsByUser) {
    if (reads.length >= ANOMALY_THRESHOLD_BULK) {
      alerts.push({
        id: buildAlertId('bulk_download', user.orgId, vaultId, userId, String(windowBucket)),
        orgId: user.orgId,
        vaultId,
        type: 'bulk_download',
        severity: 'high',
        userId,
        description: `User ${userId} read ${reads.length} files in the last ${ANOMALY_WINDOW_MINUTES} minutes (threshold: ${ANOMALY_THRESHOLD_BULK})`,
        timestamp: new Date().toISOString(),
        metadata: {
          readCount: reads.length,
          vaultId,
          uniquePaths: [...new Set(reads.map((r) => r.resourcePath))].length,
        },
        acknowledged: false,
      });
    }
  }

  // Detect: Revoked user access attempts
  const revokedAttempts = recentEntries.filter(
    (e) => e.action === 'auth.key-lease.denied' && e.metadata?.reason === 'keys_revoked'
  );

  for (const attempt of revokedAttempts) {
    alerts.push({
      id: buildAlertId('revoked_access_attempt', user.orgId, vaultId, attempt.id),
      orgId: user.orgId,
      vaultId,
      type: 'revoked_access_attempt',
      severity: 'critical',
      userId: attempt.userId,
      description: `Revoked user ${attempt.userId} attempted to obtain key lease`,
      timestamp: attempt.timestamp,
      metadata: {
        ipAddress: attempt.ipAddress,
        userAgent: attempt.userAgent,
        vaultId,
      },
      acknowledged: false,
    });
  }

  // Detect: Access outside normal hours
  const offHoursByUser = new Map<string, AuditEntry[]>();
  for (const entry of recentEntries) {
    if (entry.outcome !== 'success' || !isAccessAction(entry.action) || !isOffHours(entry.timestamp)) {
      continue;
    }

    const existing = offHoursByUser.get(entry.userId) || [];
    existing.push(entry);
    offHoursByUser.set(entry.userId, existing);
  }

  for (const [userId, entries] of offHoursByUser) {
    alerts.push({
      id: buildAlertId('off_hours_access', user.orgId, vaultId, userId, String(windowBucket)),
      orgId: user.orgId,
      vaultId,
      type: 'off_hours_access',
      severity: entries.length >= 3 ? 'medium' : 'low',
      userId,
      description: `User ${userId} performed ${entries.length} successful access actions outside normal hours`,
      timestamp: entries[0].timestamp,
      metadata: {
        actionCount: entries.length,
        vaultId,
        actions: [...new Set(entries.map((entry) => entry.action))],
        paths: [...new Set(entries.map((entry) => entry.resourcePath).filter(Boolean))].slice(0, 10),
      },
      acknowledged: false,
    });
  }

  // Detect: Unusual access patterns (many IPs or broad resource spread)
  const recentActivityByUser = new Map<string, AuditEntry[]>();
  for (const entry of recentEntries) {
    if (entry.outcome !== 'success') {
      continue;
    }

    const existing = recentActivityByUser.get(entry.userId) || [];
    existing.push(entry);
    recentActivityByUser.set(entry.userId, existing);
  }

  for (const [userId, entries] of recentActivityByUser) {
    const uniqueIps = [...new Set(entries.map((entry) => entry.ipAddress).filter(Boolean))];
    const uniquePaths = [...new Set(entries.map((entry) => entry.resourcePath).filter(Boolean))];
    const uniqueActions = [...new Set(entries.map((entry) => entry.action))];
    const triggeredByIps = uniqueIps.length >= ANOMALY_THRESHOLD_DISTINCT_IPS;
    const triggeredByBreadth =
      uniquePaths.length >= ANOMALY_THRESHOLD_DISTINCT_PATHS && uniqueActions.length >= 4;

    if (!triggeredByIps && !triggeredByBreadth) {
      continue;
    }

    alerts.push({
      id: buildAlertId('unusual_pattern', user.orgId, vaultId, userId, String(windowBucket)),
      orgId: user.orgId,
      vaultId,
      type: 'unusual_pattern',
      severity: triggeredByIps && uniqueIps.length >= ANOMALY_THRESHOLD_DISTINCT_IPS + 1 ? 'high' : 'medium',
      userId,
      description: triggeredByIps
        ? `User ${userId} accessed the vault from ${uniqueIps.length} IPs in ${ANOMALY_WINDOW_MINUTES} minutes`
        : `User ${userId} touched ${uniquePaths.length} unique resources across ${uniqueActions.length} action types in ${ANOMALY_WINDOW_MINUTES} minutes`,
      timestamp: entries[0].timestamp,
      metadata: {
        uniqueIps,
        vaultId,
        uniquePathCount: uniquePaths.length,
        uniqueActions,
      },
      acknowledged: false,
    });
  }

  for (const alert of alerts) {
    await persistAlert(alert);
  }

  return alerts;
}

