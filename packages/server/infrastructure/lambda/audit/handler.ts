function sortAlerts(alerts: SecurityAlert[]): SecurityAlert[] {
  const severityOrder: Record<string, number> = { critical: 0, high: 1, medium: 2, low: 3 };
  return [...alerts].sort((a, b) => {
    const timeDiff = new Date(b.timestamp).getTime() - new Date(a.timestamp).getTime();
    if (timeDiff !== 0) return timeDiff;
    return (severityOrder[a.severity] || 4) - (severityOrder[b.severity] || 4);
  });
}

function buildAlertId(...parts: string[]): string {
  const seed = parts.join('|');
  return `alert_${Buffer.from(seed, 'utf8').toString('base64url').slice(0, 180)}`;
}

async function persistAlert(alert: SecurityAlert): Promise<void> {
  try {
    await docClient.send(new PutCommand({
      TableName: ALERTS_TABLE,
      Item: alert,
      ConditionExpression: 'attribute_not_exists(id)',
    }));
  } catch (err) {
    const errorName = (err as { name?: string }).name;
    if (errorName !== 'ConditionalCheckFailedException') {
      console.error('[AUDIT_ALERT_PERSIST_ERROR]', (err as Error).message, {
        alertId: alert.id,
        type: alert.type,
      });
    }
  }
}

function isAccessAction(action: string): boolean {
  return (
    action.startsWith('files.')
    || action === 'auth.login'
    || action === 'auth.key-lease.issued'
    || action === 'auth.key-lease.scoped'
  );
}

function isOffHours(timestamp: string): boolean {
  const hour = new Date(timestamp).getUTCHours();

  if (OFF_HOURS_START_HOUR_UTC === OFF_HOURS_END_HOUR_UTC) {
    return false;
  }

  if (OFF_HOURS_START_HOUR_UTC < OFF_HOURS_END_HOUR_UTC) {
    return hour >= OFF_HOURS_START_HOUR_UTC && hour < OFF_HOURS_END_HOUR_UTC;
  }

  return hour >= OFF_HOURS_START_HOUR_UTC || hour < OFF_HOURS_END_HOUR_UTC;
}

/**
 * Generates a CSV string from audit log entries.
 *
 * @param entries - Array of audit entries to serialize
 * @returns CSV-formatted string with header row
 */
function generateCsv(entries: AuditEntry[]): string {
  const headers = ['id', 'timestamp', 'vaultId', 'userId', 'userEmail', 'action', 'resourcePath', 'outcome', 'ipAddress', 'userAgent', 'metadata'];
  const rows = entries.map((entry) => {
    return [
      escapeCsvField(entry.id),
      escapeCsvField(entry.timestamp),
      escapeCsvField(entry.vaultId || ''),
      escapeCsvField(entry.userId),
      escapeCsvField(entry.userEmail || ''),
      escapeCsvField(entry.action),
      escapeCsvField(entry.resourcePath),
      escapeCsvField(entry.outcome),
      escapeCsvField(entry.ipAddress || ''),
      escapeCsvField(entry.userAgent || ''),
      escapeCsvField(entry.metadata ? JSON.stringify(entry.metadata) : ''),
    ].join(',');
  });

  return [headers.join(','), ...rows].join('\n');
}

/**
 * Escapes a field value for CSV output.
 * Wraps in quotes if the value contains commas, quotes, or newlines.
 *
 * @param field - The field value to escape
 * @returns Properly escaped CSV field
 */
function escapeCsvField(field: string): string {
  if (field.includes(',') || field.includes('"') || field.includes('\n')) {
    return `"${field.replace(/"/g, '""')}"`;
  }
  return field;
}

/**
 * Generates an activity summary from audit entries.
 * Groups actions by type and calculates statistics.
 *
 * @param entries - Array of audit entries for a user
 * @returns Summary object with action counts and patterns
 */
function generateActivitySummary(entries: AuditEntry[]): Record<string, unknown> {
  const actionCounts = entries.reduce((acc, e) => {
    acc[e.action] = (acc[e.action] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);

  const outcomeCounts = entries.reduce((acc, e) => {
    acc[e.outcome] = (acc[e.outcome] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);

  const uniquePaths = [...new Set(entries.map((e) => e.resourcePath))];
  const uniqueIps = [...new Set(entries.filter((e) => e.ipAddress).map((e) => e.ipAddress!))];

  const firstActivity = entries.length > 0
    ? entries[entries.length - 1].timestamp
    : null;
  const lastActivity = entries.length > 0
    ? entries[0].timestamp
    : null;

  return {
    totalActions: entries.length,
    actionBreakdown: actionCounts,
    outcomeBreakdown: outcomeCounts,
    uniquePathsAccessed: uniquePaths.length,
    uniqueIpAddresses: uniqueIps,
    firstActivity,
    lastActivity,
    denialRate: entries.length > 0
      ? ((outcomeCounts['denied'] || 0) / entries.length * 100).toFixed(1) + '%'
      : '0%',
  };
}
