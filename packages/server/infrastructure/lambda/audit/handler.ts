function clampLimit(rawLimit: string | undefined, fallback: number, max: number): number {
  const parsed = parseInt(rawLimit || String(fallback), 10);
  if (Number.isNaN(parsed) || parsed <= 0) {
    return fallback;
  }
  return Math.min(parsed, max);
}

function normalizeStartTimestamp(value?: string): string | undefined {
  if (!value) return undefined;
  if (/^\d{4}-\d{2}-\d{2}$/.test(value)) {
    return `${value}T00:00:00.000Z`;
  }

  const parsed = new Date(value);
  return Number.isNaN(parsed.getTime()) ? value : parsed.toISOString();
}

function normalizeEndTimestamp(value?: string): string | undefined {
  if (!value) return undefined;
  if (/^\d{4}-\d{2}-\d{2}$/.test(value)) {
    return `${value}T23:59:59.999Z`;
  }

  const parsed = new Date(value);
  return Number.isNaN(parsed.getTime()) ? value : parsed.toISOString();
}

export async function applyRetentionWindow(
  orgId: string,
  startDate?: string,
  endDate?: string
): Promise<{ startDate?: string; endDate?: string }> {
  const settings = await getEffectiveOrgSettings(orgId);
  const orgRetentionDays = settings?.retentionDays ?? DEFAULT_ORG_SETTINGS.retentionDays;
  const retentionDays = FEATURES.advancedAudit
    ? orgRetentionDays
    : Math.min(orgRetentionDays, CE_AUDIT_RETENTION_DAYS);
  const retentionStart = new Date(Date.now() - retentionDays * 24 * 60 * 60 * 1000).toISOString();

  let effectiveStartDate = startDate;
  if (!effectiveStartDate || new Date(effectiveStartDate).getTime() < new Date(retentionStart).getTime()) {
    effectiveStartDate = retentionStart;
  }

  return {
    startDate: effectiveStartDate,
    endDate,
  };
}

function decodeCursor(cursor?: string): Record<string, unknown> | undefined {
  if (!cursor) return undefined;

  try {
    const decoded = Buffer.from(cursor, 'base64url').toString('utf8');
    const parsed = JSON.parse(decoded) as Record<string, unknown>;
    return typeof parsed === 'object' && parsed !== null ? parsed : undefined;
  } catch {
    throw new ValidationError('Invalid cursor');
  }
}

function encodeCursor(lastEvaluatedKey?: Record<string, unknown> | null): string | null {
  if (!lastEvaluatedKey) return null;
  return Buffer.from(JSON.stringify(lastEvaluatedKey), 'utf8').toString('base64url');
}

function buildTimeBoundKeyCondition(
  partitionCondition: string,
  expressionAttributeValues: Record<string, unknown>,
  startDate?: string,
  endDate?: string
): {
  keyConditionExpression: string;
  expressionAttributeValues: Record<string, unknown>;
  expressionAttributeNames?: Record<string, string>;
  filterParts: string[];
} {
  const expressionNames: Record<string, string> = {};
  let keyConditionExpression = partitionCondition;

  if (startDate && endDate) {
    keyConditionExpression += ' AND #ts BETWEEN :startDate AND :endDate';
    expressionAttributeValues[':startDate'] = startDate;
    expressionAttributeValues[':endDate'] = endDate;
    expressionNames['#ts'] = 'timestamp';
  } else if (startDate) {
    keyConditionExpression += ' AND #ts >= :startDate';
    expressionAttributeValues[':startDate'] = startDate;
    expressionNames['#ts'] = 'timestamp';
  } else if (endDate) {
    keyConditionExpression += ' AND #ts <= :endDate';
    expressionAttributeValues[':endDate'] = endDate;
    expressionNames['#ts'] = 'timestamp';
  }

  return {
    keyConditionExpression,
    expressionAttributeValues,
    expressionAttributeNames: Object.keys(expressionNames).length > 0 ? expressionNames : undefined,
    filterParts: [],
  };
}

function buildAuditFilterExpression(
  filters: AuditQueryFilters,
  expressionAttributeValues: Record<string, unknown>,
  expressionAttributeNames: Record<string, string>
): string | undefined {
  const filterParts: string[] = [];

  if (filters.userId) {
    filterParts.push('userId = :userId');
    expressionAttributeValues[':userId'] = filters.userId;
  }

  if (filters.action) {
    filterParts.push('#action = :action');
    expressionAttributeValues[':action'] = filters.action;
    expressionAttributeNames['#action'] = 'action';
  }

  if (filters.resourcePath) {
    filterParts.push('begins_with(resourcePath, :path)');
    expressionAttributeValues[':path'] = filters.resourcePath;
  }

  if (filters.outcome) {
    filterParts.push('outcome = :outcome');
    expressionAttributeValues[':outcome'] = filters.outcome;
  }

  if (filters.search) {
    filterParts.push(
      '(contains(userId, :search) OR contains(resourcePath, :search) OR contains(ipAddress, :search) OR contains(#action, :search) OR contains(userAgent, :search))'
    );
    expressionAttributeValues[':search'] = filters.search;
    expressionAttributeNames['#action'] = 'action';
  }

  return filterParts.length > 0 ? filterParts.join(' AND ') : undefined;
}

function buildVaultAuditQuery(orgId: string, vaultId: string, filters: AuditQueryFilters): {
  keyConditionExpression: string;
  expressionAttributeValues: Record<string, unknown>;
  expressionAttributeNames?: Record<string, string>;
  filterExpression?: string;
} {
  const query = buildTimeBoundKeyCondition(
    'vaultId = :vaultId',
    { ':vaultId': vaultId },
    filters.startDate,
    filters.endDate
  );
  const expressionNames = { ...(query.expressionAttributeNames || {}) };
  query.expressionAttributeValues[':orgId'] = orgId;
  const filterExpression = [
    'orgId = :orgId',
    buildAuditFilterExpression(filters, query.expressionAttributeValues, expressionNames),
  ].filter(Boolean).join(' AND ');

  return {
    keyConditionExpression: query.keyConditionExpression,
    expressionAttributeValues: query.expressionAttributeValues,
    expressionAttributeNames: Object.keys(expressionNames).length > 0 ? expressionNames : undefined,
    filterExpression,
  };
}

async function queryAuditIndex<T>(params: {
  indexName: string;
  keyConditionExpression: string;
  expressionAttributeValues: Record<string, unknown>;
  expressionAttributeNames?: Record<string, string>;
  filterExpression?: string;
  limit: number;
  cursor?: Record<string, unknown>;
}): Promise<PaginatedResult<T>> {
  const items: T[] = [];
  let lastEvaluatedKey = params.cursor;

  do {
    const request: Record<string, unknown> = {
      TableName: AUDIT_TABLE,
      IndexName: params.indexName,
      KeyConditionExpression: params.keyConditionExpression,
      ExpressionAttributeValues: params.expressionAttributeValues,
      Limit: Math.max(1, params.limit - items.length),
      ScanIndexForward: false,
    };

    if (params.filterExpression) {
      request.FilterExpression = params.filterExpression;
    }

    if (params.expressionAttributeNames && Object.keys(params.expressionAttributeNames).length > 0) {
      request.ExpressionAttributeNames = params.expressionAttributeNames;
    }

    if (lastEvaluatedKey) {
      request.ExclusiveStartKey = lastEvaluatedKey;
    }

    const result = await docClient.send(new QueryCommand(request as any));
    items.push(...((result.Items || []) as T[]));
    lastEvaluatedKey = result.LastEvaluatedKey as Record<string, unknown> | undefined;
  } while (items.length < params.limit && lastEvaluatedKey);

  return {
    items,
    nextCursor: encodeCursor(lastEvaluatedKey),
    lastEvaluatedKey: lastEvaluatedKey || null,
  };
}

