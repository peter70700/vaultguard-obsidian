/**
 * Lambda entry point. Routes audit/monitoring requests based on
 * HTTP method and resource path.
 *
 * @param event - API Gateway proxy event
 * @returns API Gateway proxy result with JSON body
 */
export async function handler(event: APIGatewayProxyEvent): Promise<APIGatewayProxyResult> {
  const requestId = event.requestContext?.requestId || generateId();
  const method = event.httpMethod?.toUpperCase();
  const resource = event.resource || '';

  if (!FEATURES.advancedAudit && isAdvancedAuditRoute(method, resource)) {
    return formatError(404, `Route not found: ${method} ${resource}`, requestId);
  }

  try {
    // Authenticate all requests
    const user = await verifyActiveUser(event);
    const vaultId = event.pathParameters?.vaultId || '';

    if (!vaultId) {
      return formatError(400, 'vaultId path parameter is required for audit routes', requestId);
    }

    await requireAuditVaultAdmin(event, user, vaultId, resource);

    switch (true) {
      case method === 'GET' && (resource === '/vaults/{vaultId}/audit/logs' || resource === '/vaults/{vaultId}/audit'):
        return await handleQueryLogs(event, user, vaultId, requestId);

      case method === 'GET' && resource === '/vaults/{vaultId}/audit/user/{userId}':
        return await handleUserActivity(event, user, vaultId, requestId);

      case method === 'GET' && resource === '/vaults/{vaultId}/audit/file/{path+}':
        return await handleFileAccess(event, user, vaultId, requestId);

      case method === 'GET' && resource === '/vaults/{vaultId}/audit/alerts':
        return await handleGetAlerts(event, user, vaultId, requestId);

      case method === 'POST' && (resource === '/vaults/{vaultId}/audit/export' || resource === '/vaults/{vaultId}/audit/report'):
        return await handleExport(event, user, vaultId, requestId);

      default:
        return formatError(404, `Route not found: ${method} ${resource}`, requestId);
    }
  } catch (err: unknown) {
    if (err instanceof AuthError) {
      return formatError(err.statusCode, err.message, requestId);
    }
    if (err instanceof ValidationError) {
      return formatError(err.statusCode, err.message, requestId);
    }

    console.error('[AUDIT_HANDLER_ERROR]', (err as Error).message);
    return formatError(500, 'Internal server error', requestId);
  }
}

function isAdvancedAuditRoute(method: string | undefined, resource: string): boolean {
  return (
    (method === 'GET' && resource === '/vaults/{vaultId}/audit/user/{userId}') ||
    (method === 'GET' && resource === '/vaults/{vaultId}/audit/file/{path+}') ||
    (method === 'GET' && resource === '/vaults/{vaultId}/audit/alerts') ||
    (method === 'POST' && (resource === '/vaults/{vaultId}/audit/export' || resource === '/vaults/{vaultId}/audit/report'))
  );
}

async function requireAuditVaultAdmin(
  event: APIGatewayProxyEvent,
  user: UserContext,
  vaultId: string,
  resource: string
): Promise<void> {
  try {
    await requireVaultMember(user, vaultId, 'admin');
  } catch (err) {
    await logAudit({
      userId: user.userId,
      userEmail: user.email,
      orgId: user.orgId,
      vaultId,
      action: 'audit.access.denied',
      resourcePath: resource || `/vaults/${vaultId}/audit`,
      outcome: 'denied',
      ipAddress: getClientIp(event),
      userAgent: getUserAgent(event),
      metadata: { vaultId, reason: (err as Error).message },
    });
    throw err;
  }
}

